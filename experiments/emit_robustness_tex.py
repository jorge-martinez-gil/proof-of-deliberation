"""Emit LaTeX (macros + table) from /tmp/rob/robustness_summary.json.
Numbers are produced ONLY from the committed per-run CSVs -- same philosophy as
the paper's macro pipeline. Run after analyze_robustness.py."""
import json, os
J=json.load(open("/tmp/rob/robustness_summary.json"))
by={(r["dataset"],r["learner"]):r for r in J}
ROWS=["AL","StaticGating","AdaptiveGating","Raykar","MACE","PoD-NoVigilance"]
DISP={"AL":r"\textsc{AL}","StaticGating":r"\textsc{StaticGating}",
      "AdaptiveGating":r"\textsc{AdaptiveGating}","Raykar":r"\textsc{Raykar}",
      "MACE":r"\textsc{MACE}","PoD-NoVigilance":r"\textsc{PoD-NoVig.}"}
LRN=["sgd","mlp","nb"]; LDISP={"sgd":"SGD (lin.)","mlp":"MLP (nonlin.)","nb":"GaussNB (gen.)"}
def cell(rr,c):
    cc=rr["competitors"][c]; star="\\textbf{*}" if cc["pod_wins_sig"] else ""
    return f"{cc['d_z']:.2f}{star}"
out=[]
out.append("% AUTO-GENERATED by experiments/emit_robustness_tex.py from")
out.append("% /tmp/rob/robustness_summary.json (per-run CSVs, n=10). DO NOT hand-edit.")
# macros
n=by[("gas","sgd")]["n_runs"]
out.append(f"\\newcommand{{\\RobRuns}}{{\\ensuremath{{{n}}}}}")
for lr in LRN:
    out.append(f"\\newcommand{{\\RobPoD{lr.upper()}}}{{\\ensuremath{{{by[('gas',lr)]['pod_mean_final_f1']:.3f}}}}}")
out.append(f"\\newcommand{{\\RobMLPdzAL}}{{\\ensuremath{{{by[('gas','mlp')]['competitors']['AL']['d_z']:.2f}}}}}")
out.append(f"\\newcommand{{\\RobMLPdzStatic}}{{\\ensuremath{{{by[('gas','mlp')]['competitors']['StaticGating']['d_z']:.2f}}}}}")
# table
out.append(r"\begin{table}[t]")
out.append(r"\centering")
out.append(r"\caption{Base-learner robustness on Gas Drift (SGD-specificity check). "
           r"Paired Cohen's $d_z$ of PoD vs.\ each competitor on the same $n=\RobRuns$ seed-matched "
           r"runs and identical protocol, with only the incremental base learner swapped "
           r"(published linear averaged-SGD; a nonlinear MLP; a generative Gaussian NB). "
           r"\textbf{*}: PoD significantly better after Benjamini--Hochberg FDR control ($q=0.05$) "
           r"within each learner's family. PoD's mean final-window F1 is $\RobPoDSGD$ / $\RobPoDMLP$ / "
           r"$\RobPoDNB$. No competitor beats PoD under any learner.}")
out.append(r"\label{tab:robustness-learner}")
out.append(r"\setlength{\tabcolsep}{5pt}\small")
out.append(r"\begin{tabular}{@{}lccc@{}}")
out.append(r"\toprule")
out.append(r"$d_z$: PoD vs. & "+" & ".join(LDISP[l] for l in LRN)+r" \\")
out.append(r"\midrule")
for c in ROWS:
    out.append(DISP[c]+" & "+" & ".join(cell(by[("gas",lr)],c) for lr in LRN)+r" \\")
out.append(r"\bottomrule")
out.append(r"\end{tabular}")
out.append(r"\end{table}")
txt="\n".join(out)+"\n"
open("/sessions/sharp-elegant-babbage/mnt/outputs/robustness_block.tex","w").write(txt)
print(txt)
